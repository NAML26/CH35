package Ejecicios_control;

public class ContinueLoop {

	public static void main(String[] args) {
		/* Instrucciones de salto;
		 * 
		 * continue: Interrumpe la iteracion del bucle, si procede una condición específica y continúa con la siguiente. 
		 *
COn continue se ejecuta la sentencia condicional pero cumpliendo la condicion del bucle for.
		 */
		
		for (int cliente = 1; cliente <= 10; cliente++) {
			if(cliente ==5) {
				System.out.println("Cliente numero "+cliente + " Ganaste!");
				continue;
			}
				System.out.println("Eres el cliente numero " +cliente);
			}
		}
		
}
